public class do_while {
    public static void main(String[] args) {
        // do_while loop (printing values from 0 to 10)
        int i = 1;
        int sum = 0;
        do{
            System.out.print("-> ");
            
            System.out.println(i);
            sum = sum+i;
            i = i +1;
            
            
            System.out.println(sum);
           
        }while(i<5);
          // semicolon must be here
        
    }
    
}

